<template>
    <transition name="photo_modal">
        <div class="photo_modal text-center">
            <div class="photo_modal_dialog panel">
                <img @click="close" class="close_btn" src="/static/images/icon-no-btn.png">
                <swiper :options="swiperOption" ref="mySwiper">
                    <swiper-slide class="photo_fl fl" v-for="(img,index) in swiperImgs" v-bind:key="index">
                        <img :src="img" width="900" height="475">
                    </swiper-slide>
                    <div class="swiper-button-prev" slot="button-prev"></div>
                    <div class="swiper-button-next" slot="button-next"></div>
                </swiper>
            </div>
        </div>
    </transition>
</template>

<script>
import { swiper, swiperSlide } from 'vue-awesome-swiper'

export default {
    components: {
        swiper,
        swiperSlide
    },
    data() {
        return {
            photoInfo: {},
            swiperOption: {
                notNextTick: true,
                setWrapperSize: false,
                slidesPerView: 1,
                paginationClickable: false,
                mousewheelControl: true,
                observeParents: true,
                prevButton: '.swiper-button-prev',
                nextButton: '.swiper-button-next',
                // Disable preloading of all images
                preloadImages: false,
                // Enable lazy loading
                lazyLoading: true,
                onTransitionStart(swiper) {
                    this.index = swiper.realIndex
                }
            },
        }
    },
    props: {
        swiperImgs: {
            type: Array,
            default: []
        }
    },
    computed: {
        swiper() {
            return this.$refs.mySwiper.swiper
        }
    },
    mounted() {
        this.swiper.slideTo(0, 1000, false)
    },
    methods: {
        close() {
            this.$emit('closeSwiper');
        }
    },
    watch: {
        value() {
            if (this.value) {
                const self = this;
                const tempImage = new Image();
                tempImage.onload = function () {
                    self.loading = true;
                };
                tempImage.onerror = function () {
                    self.close();
                };
                tempImage.src = self.photoModal.img;
            }
        }
    }
}
</script>

